/**
 * Render 1 Lucide SVG icon inline.
 * @param {string} name - icon key trong ICONS map
 * @param {object} [opts]
 * @param {number} [opts.size=20] - cạnh px (width = height)
 * @param {number} [opts.strokeWidth=2]
 * @param {string} [opts.className] - thêm class ngoài `icon icon-<name>`
 * @param {string} [opts.color] - override stroke (mặc định currentColor — thừa kế CSS color)
 * @returns {string} HTML string
 */
export function icon(name, opts = {}) {
  const path = ICONS[name];
  if (!path) {
    console.warn(`[icons] Unknown icon "${name}". Available: ${Object.keys(ICONS).join(', ')}`);
    return '';
  }
  const size = opts.size ?? 20;
  const strokeWidth = opts.strokeWidth ?? 2;
  const color = opts.color ?? 'currentColor';
  const extraClass = opts.className ? ` ${opts.className}` : '';
  return [
    `<svg class="icon icon-${name}${extraClass}"`,
    ` width="${size}" height="${size}" viewBox="0 0 24 24"`,
    ` fill="none" stroke="${color}" stroke-width="${strokeWidth}"`,
    ' stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">',
    path,
    '</svg>',
  ].join('');
}

